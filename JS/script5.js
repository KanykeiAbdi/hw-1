
let a = Number(prompt("Введите целое число а:"));
let b = Number(prompt("Введите ещё одно целое число b:"));

let sum = a + b;
alert("Сумма чисел: " + sum);

let difference = a - b;
alert("Разница чисел: " + difference);

let multiply = a * b;
alert("Произведение чисел: " + multiply);

let quotient = a / b;
alert("Частное от деления a на b: " + quotient);

let remainder = a % b;
alert("Остаток от деления a на b: " + remainder);

let power = a ** b;
alert("Результат возведения числа a в степень b: " + power);